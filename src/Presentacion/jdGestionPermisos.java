    private void cboEstadoPermisoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboEstadoPermisoItemStateChanged
        if (!lblNumDocumento.getText().isBlank() && this.idEmpleado != -1) {
            try {
                String estado = "";
                estado = cboEstadoPermiso.getSelectedItem().toString().replace(" ", "");
                switch (estado) {
                    case "Pendientes" ->
                        estado = "P";
                    case "Aprobados" ->
                        estado = "A";
                    case "Rechazados" ->
                        estado = "R";
                    case "Culminados" ->
                        estado = "T";
                    default -> {
                    }
                }
                listarPermisosEmpleado(estado, this.idEmpleado);

                //Habilitar o deshabilitar los botones de aprobar y rechazar:
                if (estado.equals("P")) {
                    btnAprobarPermiso.setEnabled(true);
                    btnRechazar.setEnabled(true);
                    btnEliminarSolicitud.setEnabled(true);
                } else if (estado.equals("A")) {
                    btnAprobarPermiso.setEnabled(false);
                    btnRechazar.setEnabled(false);
                    btnEliminarSolicitud.setEnabled(false);
                } else if (estado.equals("R")) {
                    btnAprobarPermiso.setEnabled(true);
                    btnRechazar.setEnabled(false);
                    btnEliminarSolicitud.setEnabled(false);
                } else if (estado.equals("T")) {
                    btnAprobarPermiso.setEnabled(false);
                    btnRechazar.setEnabled(false);
                    btnEliminarSolicitud.setEnabled(false);
                }
            } catch (Exception ex) {
                Logger.getLogger(jdGestionPermisos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_cboEstadoPermisoItemStateChanged

