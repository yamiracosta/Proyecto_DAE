    private static int getMaxTableWidth(JTable table) {
        int totalWidth = 0;
        for (int column = 0; column < table.getColumnCount(); column++) {
            totalWidth += table.getColumnModel().getColumn(column).getPreferredWidth();
        }
        return totalWidth;
    }
    
