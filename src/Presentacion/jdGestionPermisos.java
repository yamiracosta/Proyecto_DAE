    private void listarPermisosEmpleado(String estado, int idemp) throws Exception {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Fecha de solicitud");
        modelo.addColumn("Tipo de permiso");
        modelo.addColumn("Descripción");
        modelo.addColumn("Fecha de inicio");
        modelo.addColumn("Fecha de término");

        ResultSet rs = objP.filtrarPermisosEmpleadoPorEstado(estado, idemp);

        Object datos[] = new Object[6];

        while (rs.next()) {
            datos[0] = rs.getInt("id");
            datos[1] = rs.getDate("fechasolicitud");
            datos[2] = rs.getString("tipopermiso");
            datos[3] = rs.getString("descripcion");
            datos[4] = rs.getDate("fechainicio");
            datos[5] = rs.getString("fechatermino");
            modelo.addRow(datos);
        }
        tblPermisos.setModel(modelo);
        
        TableColumn column = null;
        for (int i = 0; i < tblPermisos.getColumnModel().getColumnCount(); i++) {
            column = tblPermisos.getColumnModel().getColumn(i);

            // Asignar un ancho fijo a cada columna
            switch (i) {
                case 0 -> column.setPreferredWidth(10);
                case 1 -> column.setPreferredWidth(45);
                case 2 -> column.setPreferredWidth(75);
                case 3 -> column.setPreferredWidth(230);
                case 4 -> column.setPreferredWidth(45);
                case 5 -> column.setPreferredWidth(45);
            }
        }
    }

