    private void mostrarDatosBoletaPago() throws Exception {
        SimpleDateFormat formatoSalida = new SimpleDateFormat("d MMM y");
        SimpleDateFormat formatoEntrada = new SimpleDateFormat("yyyy-MM-dd");

        ResultSet rs = objBP.obtenerDatosBoletaPagoSeleccionada(this.idboletapago);

        Object datos[] = new Object[4];

        while (rs.next()) {
            if (rs.getString("montototal").equals("No registra")) {
                datos[0] = rs.getString("montototal");
            } else {
                datos[0] = "S/. " + datos[1];
            }

            Date fecUPactd = new java.sql.Date(rs.getDate("fechapactada").getTime());
            datos[1] = formatoSalida.format(fecUPactd);

            if (rs.getString("fechapago").equals("No registra")) {
                datos[2] = rs.getString("fechapago");
            } else {
                Date fecUPago = formatoEntrada.parse(rs.getString("fechapago"));
                datos[2] = formatoSalida.format(fecUPago);
            }

            datos[3] = rs.getString("estadopago");

            lblMonto.setText(datos[0].toString());
            lblFechaPactada.setText(datos[1].toString());
            lblFechaPago.setText(datos[2].toString());
            lblEstado.setText(datos[3].toString());
        }
    }

    private void mostrarNombresBeneficios() throws Exception {
        ResultSet rs = objBenf.listarNombresBeneficios();
        Object datos[] = new Object[3];
        int cont = 0;
        while (rs.next()) {
            datos[cont] = rs.getString("nombre");
            cont++;
        }
        lblGratificacion.setText(datos[0].toString() + ":");
        lblAsignacionFamiliar.setText(datos[1].toString() + ":");
        lblCTS.setText(datos[2].toString() + ":");
    }

    private void calcularMontoBeneficios() throws ParseException, Exception {
        //Gratificación (solo meses de julio y diciembre):
        /*
        Gratificación: (Sueldo bruto * número de meses trabajados desde el inicio de contrato)/6 + 9% (si es que tiene seguro de salud)
         */
        SimpleDateFormat formatoEntrada = new SimpleDateFormat("d MMM y");
        Date fecha = formatoEntrada.parse(lblFechaPactada.getText());

        Calendar calendario = Calendar.getInstance();
        calendario.setTime(fecha);
        int mes = calendario.get(Calendar.MONTH);
        mes += 1;

        if (mes == 7 || mes == 12) {
            lblCorrespondeGrati.setText("Sí");
            this.montograti = objBP.obtenerMontoGrati(this.idboletapago);
        } else {
            lblCorrespondeGrati.setText("No corresponde");
        }

        //Asignación familiar
        boolean correspondeAF = objBP.correspondeAsignacionFamiliar(this.idboletapago);
        if (correspondeAF) {
            lblCorrespondeAsignacionFamiliar.setText("Sí");
            this.montoAF = 1025 * 0.10;
        } else {
            lblCorrespondeAsignacionFamiliar.setText("No corresponde");
        }

        if (mes != 5 || mes != 11) {
            lblCorrespondeCTS.setText("No corresponde");
        } else {
            lblCorrespondeCTS.setText("Sí");
            this.montoCTS = objBP.obtenerMontoCTS(this.codcontrato, idboletapago);
        }

        //Añadir a la tabla detalle_beneficio:
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Beneficio");
        modelo.addColumn("Monto");

        if (lblCorrespondeGrati.getText().equals("Sí")) {
            modelo.addRow(new Object[]{lblGratificacion.getText().replace(":", ""),
                "S/. " + String.format("%.2f", this.montograti)});
        }

        if (lblCorrespondeAsignacionFamiliar.getText().equals("Sí")) {
            modelo.addRow(new Object[]{lblAsignacionFamiliar.getText().replace(":", ""),
                "S/. " + String.format("%.2f", this.montoAF)});
        }

        if (lblCorrespondeCTS.getText().equals("Sí")) {
            modelo.addRow(new Object[]{lblCTS.getText().replace(":", ""),
                "S/. " + String.format("%.2f", this.montoCTS)});
        }
        tblDetalleBeneficio.setModel(modelo);
    }

