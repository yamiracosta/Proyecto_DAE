    private void listarDescuentos() throws Exception{
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Descuento");
        modelo.addColumn("Monto");
        modelo.addColumn("Descripción adicional");
        
        int numtard = objDesc.obtenerNumTardanzas(this.codcontrato, this.idboletapago);
        int numinasist = objDesc.obtenerNumInasistencias(this.codcontrato, this.idboletapago);
        
        lblNumTardanzas.setText(String.valueOf(numtard));
        lblNumInasistencias.setText(String.valueOf(numinasist));
        
        modelo.addRow(new Object[]{"Tardanza","S/. "+String.format("%.2f", numtard*1.13),"Ninguna"});
        modelo.addRow(new Object[]{"Inasistencia","S/. "+String.format("%.2f", numinasist*1.37),"Ninguna"});
        
        //Cargar las filas con beneficios o servicios adicionales:
        ResultSet rs = objDesc.obtenerDetalleServicioBeneficioAdicional(this.idboletapago);
        
        Object datos[] = new Object[3];
        while (rs.next()) {
            datos[0] = rs.getString("nombre");
            datos[1] = "S/. "+ String.format("%.2f", rs.getDouble("monto"));
            datos[2] = rs.getString("descripcion");
            modelo.addRow(datos);
        }
        
        tblDetalleDescuento.setModel(modelo);
    }
    
