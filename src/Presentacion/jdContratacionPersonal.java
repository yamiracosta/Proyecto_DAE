    //botón de buscar empleado
    private void buscarEmpleado() {
        jdVentanaBuscarEmpleado obj = new jdVentanaBuscarEmpleado((Frame) SwingUtilities.getWindowAncestor(this), true);
        obj.setLocationRelativeTo(this);
        obj.setVisible(true);

        if (obj.getApepat() != null && obj.getApepat() != null && obj.getNomarea() != null && obj.getNombres() != null && obj.getNumdoc() != null
                && obj.getTipodoc() != null && obj.getTipoemp() != null) {
            lblApeMaterno.setText(obj.getApemat());
            lblApePaterno.setText(obj.getApepat());
            lblArea.setText(obj.getNomarea());
            lblNombres.setText(obj.getNombres());
            lblNumDocumento.setText(obj.getNumdoc());
            lblTipoDoc.setText(obj.getTipodoc());
            lblTipoEmpleado.setText(obj.getTipoemp());
            try {
                int id = objE.obtenerIdEmpleado(lblNumDocumento.getText(), lblTipoDoc.getText());
                setearLblVacacionesPagadas(id);
            } catch (Exception ex) {
                Logger.getLogger(jdContratacionPersonal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private void setearLblVacacionesPagadas(int idemp) throws Exception {
        boolean cumple = objE.tieneContratosAnterioresValidos(idemp);
        if (cumple) {
            lblDerechoVacaciones.setText("Sí");
        } else {
            lblDerechoVacaciones.setText("No");
        }
    }

    private String getTextoTurno(String cboHorarioItem) {

        Pattern pattern = Pattern.compile("^(Mañana|Tarde|Noche) \\(");
        Matcher matcher = pattern.matcher(cboHorarioItem);
        if (matcher.find()) {
            return matcher.group(1); // Grupo 1 es el turno (Mañana o Tarde)
        }
        return ""; // Retornar cadena vacía si no se encuentra coincidencia
    }

