    private void cargarCboTipoContrato() throws Exception {
        ResultSet rs = objTC.cargarTipoContratos();
        while (rs.next()) {
            cboTipoContrato.addItem(rs.getString("nombre"));
        }
    }

    //Según el tipo de contrato seleccionado, mostrar los horarios disponibles:
    private void cargarCboHorariosPorTipoContrato() throws Exception {
        cboHorario.removeAllItems();
        SimpleDateFormat timeFormat = new SimpleDateFormat("h:mm a");

        String tipocontrato = cboTipoContrato.getSelectedItem().toString();
        ResultSet rs = objTC.cargarHorariosDeTipoContrato(tipocontrato);
        while (rs.next()) {
            String turno = rs.getString("turno");
            String horainicio = timeFormat.format(rs.getTime("horaentrada"));
            String horafin = timeFormat.format(rs.getTime("horasalida"));
            cboHorario.addItem(turno + " (" + horainicio + " - " + horafin + ")");
        }
    }

    private void cargarCboPeriodoPago() throws Exception {
        ResultSet rs = objPP.cargarPeriodosPago();
        while (rs.next()) {
            cboPeriodoPago.addItem(rs.getString("nombre"));
        }
    }

