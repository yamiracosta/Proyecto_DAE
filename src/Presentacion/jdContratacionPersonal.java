    private void pasarDatos() throws ParseException {
        SimpleDateFormat formato = new SimpleDateFormat("dd MMM yyyy");

        tipoemp = lblTipoEmpleado.getText();

        jdVentanaCalcularRetencionesPorLey obj = new jdVentanaCalcularRetencionesPorLey((Frame) SwingUtilities.getWindowAncestor(this), true);
        obj.setTipoEmp(tipoemp);
        obj.setTipoContrato(cboTipoContrato.getSelectedItem().toString());
        obj.setPeriodo(cboPeriodoPago.getSelectedItem().toString());

        //Trabajador
        obj.setApepatRSMN(lblApePaterno.getText());
        obj.setApematRSMN(lblApeMaterno.getText());
        obj.setNombresRSMN(lblNombres.getText());
        obj.setTipoempRSMN(lblTipoEmpleado.getText());
        obj.setNumdocRSMN(lblNumDocumento.getText());
        obj.setTipdocRSMN(lblTipoDoc.getText());
        obj.setAreaRSMN(lblArea.getText());

        //Contrato
        //Formatear fecha de inicio
        obj.setFechainicioRSMN(formato.format(dchsrFechaInicio.getDate()));

        //Formatear fecha de término
        Date fechaTerm = null;
        fechaTerm = formato.parse(lblFechaTermino.getText());
        obj.setFechaterminoRSMN(formato.format(fechaTerm));

        obj.setTipocontRSMN(cboTipoContrato.getSelectedItem().toString());

        //Obtener valores para el turno y el horario
        //turno
        String cboitemhorario = cboHorario.getSelectedItem().toString();
        obj.setTurnoRSMN(getTextoTurno(cboitemhorario));

        //horario
        String[] parts2 = cboHorario.getSelectedItem().toString().split(" \\(");
        if (parts2.length > 1) {
            // Remover el paréntesis de cierre al final
            obj.setHorariotrabajoRSMN(parts2[1].replace(")", ""));
        }

        obj.setPeriodopagoRSMN(cboPeriodoPago.getSelectedItem().toString());
        obj.setDerechovacasRSMN(lblDerechoVacaciones.getText());

        obj.setResponsabilidadesRSMN(txtResponsabilidades.getText());

        obj.setLocationRelativeTo(this);
        obj.setVisible(true);
    }

    private void calcularSueldo() throws ParseException {
        if (dchsrFechaInicio.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Especifique la duración del contrato");
        } else {
            pasarDatos();
        }
    }

