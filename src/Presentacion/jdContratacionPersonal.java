    //Datos de contrato
    public void instanciarDatosContrato() throws ParseException {
        this.tipocont = cboTipoContrato.getSelectedItem().toString();

        SimpleDateFormat inputFormat = new SimpleDateFormat("dd MMM yyyy");
        SimpleDateFormat formato = new SimpleDateFormat("dd-MM-yyyy");

        //Formatear fecha de inicio a dd-mm-yyyy
        this.fechainicio = formato.format((Date) dchsrFechaInicio.getDate());

        //Formatear fecha de término a dd-mm-yyyy
        Date fechaTerm = inputFormat.parse(lblFechaTermino.getText());
        this.fechatermino = formato.format(fechaTerm);

        this.periodopago = cboPeriodoPago.getSelectedItem().toString();

        //Obtener valores para el turno y el horario
        //turno
        String[] parts = cboHorario.getSelectedItem().toString().split(" \\(");
        this.turno = parts[0];

        //horario
        String[] parts2 = cboHorario.getSelectedItem().toString().split(" \\(");
        if (parts2.length > 1) {
            // Remover el paréntesis de cierre al final
            this.horariotrabajo = parts2[1].replace(")", "");
        }

        this.derechovacas = lblDerechoVacaciones.getText();
        this.responsabilidades = txtResponsabilidades.getText();

    }

