    private void mostrarDatosContratoVigente() throws Exception{
        String numdoc = tblEmpleados.getValueAt(tblEmpleados.getSelectedRow(), 0).toString();
        int idtipodoc = objTD.obtenerIdTipoDocumento(tblEmpleados.getValueAt(tblEmpleados.getSelectedRow(), 1).toString());
        
        ResultSet rs = objC.obtenerDatosContratoTrabajador(numdoc, idtipodoc);
        
        String nombres = tblEmpleados.getValueAt(tblEmpleados.getSelectedRow(), 4).toString();
        String apep = tblEmpleados.getValueAt(tblEmpleados.getSelectedRow(), 2).toString();
        String apem = tblEmpleados.getValueAt(tblEmpleados.getSelectedRow(), 3).toString();
        String nombreEmpleado = apep + " " + apem + " " + nombres;
        
        lblEmpleado.setText(nombreEmpleado);
        
        Object datos[] = new Object[8];
        
        while (rs.next()) {
            this.codcontrato=rs.getInt("codigocontrato");
            datos[0] = rs.getDate("fechainicio"); //Fecha de inicio
            datos[1] = rs.getDate("fechatermino"); //Fecha de término
            datos[2] = rs.getString("periodopago"); //Periodo de pago
            datos[3] = rs.getString("tipocontrato"); //Tipo de contrato
            datos[4] = rs.getString("horario_trabajo"); //Horario de trabajo
            datos[5] = rs.getString("derechovacas"); //Derecho a vacaciones pagadas
            datos[6] = rs.getString("responsabilidades"); //Responsabilidades
            datos[7] = rs.getString("turno"); //Turno
        }
        
        SimpleDateFormat formatoEntrada = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat formatoSalida = new SimpleDateFormat("d MMM y");
        
        Date fechaIni = formatoEntrada.parse(datos[0].toString());
        Date fechaFin = formatoEntrada.parse(datos[1].toString());
        
        lblFechaInicio.setText(formatoSalida.format(fechaIni));
        lblFechaTermino.setText(formatoSalida.format(fechaFin));
        lblPeriodoPago.setText(datos[2].toString());
        lblTipoContrato.setText(datos[3].toString());
        lblHorarioTrabajo.setText(datos[7].toString()+" ("+datos[4].toString()+")");
        lblDerechoVacasPagadas.setText(datos[5].toString());
        txtResponsabilidades.setText(datos[6].toString());
    }
    
