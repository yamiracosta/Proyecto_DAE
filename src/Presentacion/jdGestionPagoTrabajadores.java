    private void listarPagos() throws Exception{
        ResultSet rs = objBP.listarPagosContratoTrabajador(this.codcontrato);
        boolean excede = objBP.verificarNumMaximoBoletasPago(this.codcontrato);
        
        SimpleDateFormat formatoSalida = new SimpleDateFormat("d MMM y");
        SimpleDateFormat formatoEntrada = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat formatoEntradaFechaPago = new SimpleDateFormat("dd-MM-yyyy");
        
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Monto total");
        modelo.addColumn("Fecha pactada");
        modelo.addColumn("Fecha de pago");
        modelo.addColumn("Estado");
        
        Object datos[] = new Object[5];
        
        while (rs.next()) {
            datos[0] = rs.getInt("id");
            
            if (rs.getString("montototal").equals("No registra")) {
                datos[1] = rs.getString("montototal");
            } else {
                datos[1] = "S/. "+rs.getString("montototal");
            }
            
            Date fecUPactd = new java.sql.Date(rs.getDate("fechapactada").getTime());
            datos[2] = formatoSalida.format(fecUPactd);
            
            if (rs.getString("fechapago").equals("No registra")) {
                datos[3] = rs.getString("fechapago");
            } else {
                Date fecUPago = formatoEntradaFechaPago.parse(rs.getString("fechapago"));
                datos[3] = formatoSalida.format(fecUPago);
            }
            
            datos[4] = rs.getString("estadopago");
            modelo.addRow(datos);
        }
        tblPagos.setModel(modelo);
        
        if (excede) {
            btnGenerarBoletaPago.setEnabled(false);
        } else {
            btnGenerarBoletaPago.setEnabled(true);
        }
    }
    
