    private void updateDateTime() {
        Date now = new Date();

        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        SimpleDateFormat timeFormat = new SimpleDateFormat("hh:mm a");

        lblFecha.setText(dateFormat.format(now));
        lblHora.setText(timeFormat.format(now));
    }

    private void setearFechaHoraActual() {
        Timer timer = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                updateDateTime();
            }
        });
        timer.start();
    }

    //Intercambiar paneles
    public void showPanel(String panelName) {
        mainPanel.removeAll();
        if (null != panelName) {
            switch (panelName) {
                case "panelM" ->
                    mainPanel.add(pm);
                case "panelT" ->
                    mainPanel.add(pt);
                case "panelR" ->
                    mainPanel.add(pr);
                case "panelB" ->
                    mainPanel.add(pb);
                default -> {
                }
            }
        }
        mainPanel.revalidate();
        mainPanel.repaint();
    }

    public void identificarUsuario(JTextField usuario) throws Exception {
        Object[] datos = recono.identificarUsuario(usuario);

        lblPuestoUsuario.setText(datos[0].toString().toUpperCase());
        lblNombreUsuario.setText(datos[1].toString());

        switch (lblPuestoUsuario.getText()) {
            case "ADMINISTRADOR" -> {
                String imagePath = "/Imagenes/administrador.jpg";
                ImageIcon icon = new ImageIcon(getClass().getResource(imagePath));
                img_tipo_usuario.setIcon(icon);
                habilitarOpciones("ADMINISTRADOR");
            }
            case "JEFE DE RR.HH." -> {
                String imagePath = "/Imagenes/RH_1.png";
                ImageIcon icon = new ImageIcon(getClass().getResource(imagePath));
                img_tipo_usuario.setIcon(icon);
                habilitarOpciones("JEFE DE RR.HH.");
            }
            default -> {
                String imagePath = "/Imagenes/GOperaciones.png";
                ImageIcon icon = new ImageIcon(getClass().getResource(imagePath));
                img_tipo_usuario.setIcon(icon);
                habilitarOpciones("JEFE DE OPERACIONES");
                //
            }
        }
    }

