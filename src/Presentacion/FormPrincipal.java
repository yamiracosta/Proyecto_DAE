    private void habilitarOpciones(String puesto) {
        switch (puesto) {
            case "ADMINISTRADOR":
                //Tiene acceso a todo
                btnGestionarCredenciales.setEnabled(true);
                btnMantenimiento.setEnabled(true);
                pm.accesoManteArea(true);
                pm.accesoManteBeneficio(true);
                pm.accesoManteDeduccion(true);
                pm.accesoManteDescuento(true);
                pm.accesoManteEmpleado(true);
                pm.accesoManteHorarioTrabajo(true);
                pm.accesoMantePeriodoPago(true);
                pm.accesoManteTipoContrato(true);
                pm.accesoManteTipoDeduccion(true);
                pm.accesoManteTipoDocumento(true);
                pm.accesoManteTipoEmpleado(true);
                pm.accesoManteTipoPermiso(true);

                btnOperaciones.setEnabled(true);
                pt.accesoControlAsistencia(true);
                pt.accesoCulminacionContrato(true);
                pt.accesoGestionFechaVacaciones(true);
                pt.accesoGestionFondoPensionSeguroSalud(true);
                pt.accesoGestionPagoTrabajador(true);
                pt.accesoGestionPermisos(true);
                pt.accesoRegistroContrato(true);
                
                break;
            case "JEFE DE RR.HH.":
                //Acceso a contratos, operaciones relacionadas a ellos, y acceso a ciertos reportes:
                btnGestionarCredenciales.setEnabled(false);
                btnMantenimiento.setEnabled(false); //NingÃºn acceso a mantenimiento
             

                btnOperaciones.setEnabled(true); //Acceso a todos menos a gestiÃ³n de retenciones por ley y gestiÃ³n de pago de trabajadores
                pt.accesoControlAsistencia(true);
                pt.accesoCulminacionContrato(true);
                pt.accesoGestionFechaVacaciones(true);
                pt.accesoGestionFondoPensionSeguroSalud(false);
                pt.accesoGestionPagoTrabajador(false);
                pt.accesoGestionPermisos(true);
                pt.accesoRegistroContrato(true);
                
                break;
            
            case "JEFE DE OPERACIONES":
                
                //Jefe de Operaciones: Acceso a gestiÃ³n de pagos y reportes de gastos
                btnGestionarCredenciales.setEnabled(false);
                btnMantenimiento.setEnabled(false);
                btnOperaciones.setEnabled(true); //Acceso a todos menos a gestiÃ³n de retenciones por ley y gestiÃ³n de pago de trabajadores
                pt.accesoControlAsistencia(false);
                pt.accesoCulminacionContrato(false);
                pt.accesoGestionFechaVacaciones(false);
                pt.accesoGestionFondoPensionSeguroSalud(true);
                pt.accesoGestionPagoTrabajador(true);
                pt.accesoGestionPermisos(false);
                pt.accesoRegistroContrato(false);
                
                break;
                
            default:
                throw new AssertionError();
        }
    }

