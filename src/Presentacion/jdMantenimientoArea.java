    private void btnAgregarAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarAreaActionPerformed
        if (txtNombreArea.getText().isBlank() || txtDescripcionArea.getText().isBlank()) {
            JOptionPane.showMessageDialog(rootPane, "Por favor, complete todos los campos");
        } else {
            try {
                agregarArea(txtNombreArea.getText(), txtDescripcionArea.getText());
                limpiar();
                JOptionPane.showMessageDialog(rootPane, "Área registrada");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Error al registrar área --> " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnAgregarAreaActionPerformed

    private void btnEditarAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarAreaActionPerformed
        if (tblAreas.getSelectedRowCount() > 0) {
            if (txtNombreArea.getText().isBlank() || txtDescripcionArea.getText().isBlank()) {
                JOptionPane.showMessageDialog(rootPane, "Por favor, complete todos los campos");
            } else {
                try {
                    if (JOptionPane.showConfirmDialog(rootPane, "¿Modificar datos?", "Confirmación de cambios", JOptionPane.YES_NO_OPTION)
                            == JOptionPane.YES_OPTION) {
                        modificarArea(txtNombreArea.getText(), txtDescripcionArea.getText(),
                                Integer.parseInt(tblAreas.getValueAt(tblAreas.getSelectedRow(), 0).toString()));
                        limpiar();
                        JOptionPane.showMessageDialog(rootPane, "Área modificada");
                    }

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, "Error al modificar área --> " + e.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un área de la lista");
        }

    }//GEN-LAST:event_btnEditarAreaActionPerformed

    private void btnEliminarAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarAreaActionPerformed
        if (tblAreas.getSelectedRowCount() <= 0) {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un área de la lista para eliminar");
        } else {
            try {
                if (JOptionPane.showConfirmDialog(rootPane, "¿Eliminar área?", "Confirmación de cambios", JOptionPane.YES_NO_OPTION)
                        == JOptionPane.YES_OPTION) {
                    String codigo = tblAreas.getValueAt(tblAreas.getSelectedRow(), 0).toString();
                    if (!obj.existenRegistrosRelacionadosArea(Integer.parseInt(codigo))) {
                        eliminarArea(Integer.parseInt(codigo));
                        limpiar();
                        JOptionPane.showMessageDialog(rootPane, "Área eliminada");
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "No se puede eliminar. El área ya está referenciada en otras tablas");
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Error al eliminar área --> " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarAreaActionPerformed

