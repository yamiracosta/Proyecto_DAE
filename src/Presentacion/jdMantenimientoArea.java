    private void fijarAncho(JTable table){
        TableColumn column = null;
        for (int i = 0; i < table.getColumnModel().getColumnCount(); i++) {
            column = table.getColumnModel().getColumn(i);

            // Asignar un ancho fijo a cada columna
            switch (i) {
                case 0 -> column.setPreferredWidth(20); // Ancho para la columna "Nombre"
                case 1 -> column.setPreferredWidth(50); // Ancho para la columna "Edad"
                case 2 -> column.setPreferredWidth(155); // Ancho para la columna "País"
            }
        }
    }

