    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (txtNumeroDocumento.getText().isBlank() | cbxTipoDocumento.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Ingrese un número y un tipo de documento válidos");
        } else {
            try {
                //Agregar método existeEmpleado a la clase MantenimientoTrabajador:
                String numdoc = txtNumeroDocumento.getText();
                String tipodoc = cbxTipoDocumento.getSelectedItem().toString();
                if (!mante.existeEmpleado(numdoc, tipodoc)) {
                    JOptionPane.showMessageDialog(rootPane, "No se encontró el empleado");
                } else {
                    buscarTrabajador(numdoc, tipodoc);
                    txtNumeroDocumento.setText("");
                    cbxTipoDocumento.setSelectedIndex(0);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "No se pudo ejecutar la búsqueda --> " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tabla_EmpleadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_EmpleadoMouseClicked
        if (tabla_Empleado.getSelectedRowCount() > 0) {
            String numdoc = tabla_Empleado.getValueAt(tabla_Empleado.getSelectedRow(), 0).toString();
            String tipodoc = tabla_Empleado.getValueAt(tabla_Empleado.getSelectedRow(), 1).toString();
            String nombres = tabla_Empleado.getValueAt(tabla_Empleado.getSelectedRow(), 2).toString();
            String apep = tabla_Empleado.getValueAt(tabla_Empleado.getSelectedRow(), 3).toString();
            String apem = tabla_Empleado.getValueAt(tabla_Empleado.getSelectedRow(), 4).toString();
            String correo = "";
            if (tabla_Empleado.getValueAt(tabla_Empleado.getSelectedRow(), 5) == null) {
                correo = "";
            } else {
                correo = tabla_Empleado.getValueAt(tabla_Empleado.getSelectedRow(), 5).toString();
            }
            
            String telefono = tabla_Empleado.getValueAt(tabla_Empleado.getSelectedRow(), 6).toString();
            
            if (tabla_Empleado.getValueAt(tabla_Empleado.getSelectedRow(), 7).toString().equals("Vigente")) {
                chbVigencia.setSelected(true);
            } else {
                chbVigencia.setSelected(false);
            }
            
            String tipoemp = tabla_Empleado.getValueAt(tabla_Empleado.getSelectedRow(), 8).toString();
            String superior = tabla_Empleado.getValueAt(tabla_Empleado.getSelectedRow(), 9).toString();
            
            if (tabla_Empleado.getValueAt(tabla_Empleado.getSelectedRow(), 10).toString().equals("Sí")) {
                ckbTieneHijos.setSelected(true);
            } else {
                ckbTieneHijos.setSelected(false);
            }

            txtNumeroDocumento.setText(numdoc);
            cbxTipoDocumento.setSelectedItem(tipodoc);
            txtNombres.setText(nombres);
            txtApePaterno.setText(apep);
            txtApeMaterno.setText(apem);
            txtCorreo.setText(correo);
            txtNumTelefono.setText(telefono);
            cbxTipoEmpleado.setSelectedItem(tipoemp);
            cbxSuperior.setSelectedItem(superior);
        }
    }//GEN-LAST:event_tabla_EmpleadoMouseClicked

