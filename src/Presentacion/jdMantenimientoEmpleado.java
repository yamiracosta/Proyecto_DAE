    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            if (txtNumeroDocumento.getText().isEmpty() | cbxTipoDocumento.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Ingrese un número y tipo de documento válidos");
            } else {
                if (JOptionPane.showConfirmDialog(rootPane, "¿Eliminar empleado?", "Confirmar cambios", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    if (!mante.existenTablasReferenciadasEmpleado(txtNumeroDocumento, cbxTipoDocumento)) {
                        mante.eliminarTrabajador(txtNumeroDocumento, cbxTipoDocumento);
                        limpiar();
                        JOptionPane.showMessageDialog(null, "El empleado fue eliminado correctamente");
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "No se puede eliminar. El empleado ya se encuentra referenciado en otras tablas");
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "No se pudo eliminar el empleado: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

