    private void btnConfirmarPagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarPagoActionPerformed
        int opcion = JOptionPane.showConfirmDialog(rootPane, "¿Registrar pago de boleta?\n\nEsta acción es irreversible", "Confirmar cambios",
                JOptionPane.YES_NO_OPTION);
        if (opcion == JOptionPane.YES_OPTION) {
            try {
                double sueldopagado = Double.parseDouble(lblSalarioNeto.getText().replace("S/. ", ""));
                double montototalbenef = Double.parseDouble(lblTotalBeneficios.getText().replace("S/. ", ""));
                double montototaldsctos = Double.parseDouble(lblTotalDescuentos.getText().replace("S/. ", ""));
                double montototalpagar = Double.parseDouble(lblMontoTotal.getText().replace("S/. ", ""));
                int idboleta = this.idboletapago;
                objBP.registrarPagoBoleta(tblDetalleBeneficio, tblDetalleDescuento, sueldopagado, montototalbenef, montototaldsctos, montototalpagar, idboletapago);
                JOptionPane.showMessageDialog(rootPane, "El pago de la boleta ha sido registrado", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                jdVentanaVerPago.dispose();
                this.dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, "Error al registrar pago de boleta --> "+ex.getMessage(), "Ha ocurrido un error",
                JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnConfirmarPagoActionPerformed

