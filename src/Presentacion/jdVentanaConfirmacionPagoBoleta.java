    private void listarTablasYMontos(){
        tblDetalleBeneficio.setModel(this.tblDetalleBenef);
        tblDetalleDescuento.setModel(this.tblDetalleDesc);
        
        double totalBenef=0;
        for (int i = 0; i < tblDetalleBeneficio.getRowCount(); i++) {
            double montoP = Double.parseDouble(tblDetalleBeneficio.getValueAt(i, 1).toString().replace("S/. ", ""));
            totalBenef+=montoP;
        }
        
        double totalDesc = 0;
        for (int i = 0; i < tblDetalleDescuento.getRowCount(); i++) {
            double montoP = Double.parseDouble(tblDetalleDescuento.getValueAt(i, 1).toString().replace("S/. ", ""));
            totalDesc += montoP;
        }
        
        lblTotalBeneficios.setText("S/. "+String.format("%.2f", totalBenef));
        lblTotalDescuentos.setText("S/. "+String.format("%.2f", totalDesc));
    }
    
    private void setearFechaActual(){
        Calendar cal = Calendar.getInstance();
        Date fechaActual = cal.getTime();
        SimpleDateFormat formato = new SimpleDateFormat("d MMM y");
        lblFechaPago.setText(formato.format(fechaActual));
    }
    
    private void calcularMontoTotalPago() throws Exception{
        double salariobr = objC.obtenerSalarioNetoEmpleado(this.codcontrato);
        lblSalarioNeto.setText("S/. "+String.format("%.2f", salariobr));
        double montoTotalBnf = Double.parseDouble(lblTotalBeneficios.getText().replace("S/. ", ""));
        double montoTotalDsc = Double.parseDouble(lblTotalDescuentos.getText().replace("S/. ", ""));
        double montoTotal = salariobr+montoTotalBnf-montoTotalDsc;
        
        lblMontoTotal.setText("S/. "+String.format("%.2f", montoTotal));
    }

