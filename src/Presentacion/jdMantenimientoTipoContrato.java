    private void listarTodo() throws Exception {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Tipo de contrato");

        ResultSet rs = tipocont.listarTodo();

        Object datos[] = new Object[2];

        while (rs.next()) {
            datos[0] = rs.getInt("id");
            datos[1] = rs.getString("nombre");
            modelo.addRow(datos);
        }
        tblTipoContrato.setModel(modelo);
    }

    private void filtrarTipoContrato(JTextField tipo) throws Exception {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Tipo de contrato");

        ResultSet rs = tipocont.filtrar(tipo.getText());

        Object datos[] = new Object[2];

        while (rs.next()) {
            datos[0] = rs.getInt("id");
            datos[1] = rs.getString("nombre");
            modelo.addRow(datos);
        }
        tblTipoContrato.setModel(modelo);
    }

    private void agregar(JTextField tipo) throws Exception {
        tipocont.registrarTipoContrato(tipo.getText());
    }

    private void modificar(String codigo, JTextField tipo) throws Exception {
        tipocont.modificarTipoContrato(Integer.parseInt(codigo), tipo.getText());
    }

    private void eliminar(String codigo) throws Exception {
        tipocont.eliminarTipoContrato(Integer.parseInt(codigo));
    }

    private void limpiarTodo() throws Exception {
        txtNombreTipoContrato.setText("");
        listarTodo();
    }

