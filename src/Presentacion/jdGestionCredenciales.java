    private void txtBuscarPorDniEmpleadoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarPorDniEmpleadoKeyTyped
        char c = evt.getKeyChar();
        if (c < '0' || c > '9') {
            evt.consume();
        }
        
        if (txtBuscarPorDniEmpleado.getText().length() >= 20) {
            evt.consume();
        }
    }//GEN-LAST:event_txtBuscarPorDniEmpleadoKeyTyped

