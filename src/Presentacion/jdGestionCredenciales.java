    private void btnRegistrarCredencialesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarCredencialesActionPerformed
        if (tblEmpleado.getSelectedRowCount() > 0) {
            String usu = txtNomUsuario.getText();
            String contra = txtContraseña.getText();
            if (JOptionPane.showConfirmDialog(rootPane, "¡IMPORTANTE!\n\nLas credenciales ingresadas no son almacenadas en el sistema. "
                    + "Por tanto, debes recordarlas o guardarlas, ya que ningún\ntrabajador adicional tiene acceso a estas. Si por algún motivo "
                    + "olvidas\ntus credenciales, deberás volver a crear nuevas credenciales\n\nNombre de usuario: "+usu+"\nContraseña: "+contra
                    + "\n\n¿Deseas continuar?", "AVISO DEL SISTEMA",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {
                    objE.registrarCredenciales(usu, contra, tblEmpleado.getValueAt(tblEmpleado.getSelectedRow(), 1).toString(),
                            tblEmpleado.getValueAt(tblEmpleado.getSelectedRow(), 2).toString());
                    JOptionPane.showMessageDialog(rootPane, "Tus credenciales han sido actualizadas");
                    this.dispose();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(rootPane, "No se pudo registrar las credenciales --> "+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un empleado de la lista para asignar las credenciales", "Error",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistrarCredencialesActionPerformed

