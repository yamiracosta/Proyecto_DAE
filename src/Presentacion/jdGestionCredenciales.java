    private void txtNomUsuarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomUsuarioKeyTyped
        if (!txtNomUsuario.getText().isBlank() && !txtContraseña.getText().isBlank()) {
            btnRegistrarCredenciales.setEnabled(true);
        } else {
            btnRegistrarCredenciales.setEnabled(false);
        }
        
        if (txtNomUsuario.getText().length() >= 30) {
            evt.consume();
        }
    }//GEN-LAST:event_txtNomUsuarioKeyTyped

    private void txtContraseñaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtContraseñaKeyTyped
        if (!txtNomUsuario.getText().isBlank() && !txtContraseña.getText().isBlank()) {
            btnRegistrarCredenciales.setEnabled(true);
        } else {
            btnRegistrarCredenciales.setEnabled(false);
        }
        
        if (txtContraseña.getText().length() >= 50) {
            evt.consume();
            btnRegistrarCredenciales.setEnabled(false);
        }
    }//GEN-LAST:event_txtContraseñaKeyTyped

    private void filtrarEmpleado(JTextField numdoc) throws Exception{
        ResultSet rs = objE.filtrarEmpleadoCredencialesDNI(numdoc.getText());
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Empleado");
        modelo.addColumn("N° documento");
        modelo.addColumn("Tipo de documento");
        
        Object datos[] = new Object[3];
        
        while (rs.next()) {
            datos[0] = rs.getString("nombres");
            datos[1] = rs.getString("numdocumento");
            datos[2] = rs.getString("tipodocumento");
            modelo.addRow(datos);
        }
        tblEmpleado.setModel(modelo);
    }
    
