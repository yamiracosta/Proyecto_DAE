    public static String codigoEstado(JComboBox estadoSeleccionado) {
        if (estadoSeleccionado.getSelectedItem().equals("Con Permiso")) {
            return "CP";
        } else if (estadoSeleccionado.getSelectedItem().equals("Asistio")) {
            return "A";
        } else if (estadoSeleccionado.getSelectedItem().equals("Falto")) {
            return "F";
        } else if (estadoSeleccionado.getSelectedItem().equals("Tardanza")) {
            return "T";
        } else if (estadoSeleccionado.getSelectedItem().equals("Tardanza Justificada")) {
            return "TJ";
        } else if (estadoSeleccionado.getSelectedItem().equals("Falta Justificada")) {
            return "FJ";
        } else if (estadoSeleccionado.getSelectedItem().equals("Vacaciones")) {
            return "V";
        } else {
            return null;
        }
    }

