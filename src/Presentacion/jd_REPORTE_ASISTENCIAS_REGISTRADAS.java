    private void btnGenerarReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarReporteActionPerformed
        try {
            if (cbxMes.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Por favor seleccione un Mes");
            } else {
                if (cbxEstado.getSelectedIndex() == 0) {
                    JOptionPane.showMessageDialog(this, "Por favor seleccione un Estado");
                } else {
                    Container contenedor = this.jdpReporte;
                    contenedor.setLayout(new BorderLayout());
                    //JOptionPane.showMessageDialog(this, "Procesando Reporte");
                    Map<String, Object> parametros = new HashMap<>();
                    parametros.put("pmt_Mes", cbxMes.getSelectedIndex());
                    //parametros.put("pmt_Dia", cbxDia.getSelectedIndex());
                    parametros.put("pmt_Estado", codigoEstado(cbxEstado));

                    System.out.println("Mes seleccionado: " + cbxMes.getSelectedIndex());
                    //System.out.println("Día seleccionado: " + cbxDia.getSelectedIndex());
                    System.out.println("Estado seleccionado: " + codigoEstado(cbxEstado));

                    JRViewer objReporte = new Reporte().reporteInterno("rptEMPLEADO_CON_TARDANZA_O_INASISTENCIA_X_MES.jasper", parametros);
                    contenedor.add(objReporte);
                    objReporte.setVisible(true);
                    //Mostrar el reporte
                    this.jdpReporte.setVisible(true);
                    //JOptionPane.showMessageDialog(this, "Reporte Mostrado");
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage() + "ERROR en Reporte", "Error", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnGenerarReporteActionPerformed


