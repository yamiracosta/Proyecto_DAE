    private void listarTodo() throws Exception {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Tipo de deducción");

        ResultSet rs = objTD.listarTodo();

        Object datos[] = new Object[2];

        while (rs.next()) {
            datos[0] = rs.getInt("id");
            datos[1] = rs.getString("tipo");
            modelo.addRow(datos);
        }
        tblTipoDeduccion.setModel(modelo);
    }

    private void filtrar(JTextField txtbuscar) throws Exception {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Tipo de deducción");

        ResultSet rs = objTD.filtrarTipoDeduccion(txtbuscar.getText());

        Object datos[] = new Object[2];

        while (rs.next()) {
            datos[0] = rs.getInt("id");
            datos[1] = rs.getString("tipo");
            modelo.addRow(datos);
        }
        tblTipoDeduccion.setModel(modelo);
    }

    private void registrar(JTextField tipo) throws Exception {
        objTD.registrarTipoDeduccion(tipo.getText());
    }

    private void modificar(JTextField tipo, String codigo) throws Exception {
        objTD.modificarTipoDeduccion(tipo.getText(), Integer.parseInt(codigo));
    }

    private void eliminar(String codigo) throws Exception {
        objTD.eliminarTipoDeduccion(Integer.parseInt(codigo));
    }

    private void limpiar() throws Exception {
        txtNombreTipoDeduccion.setText("");
        listarTodo();
    }

