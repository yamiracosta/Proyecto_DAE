    private void btnAgregarHorarioTrabajoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarHorarioTrabajoActionPerformed
        try {
            if (cboTurno.getSelectedIndex() == 0 || cboHorario.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Por favor, seleccione un turno o un horario válido");
            } else {
                agregar(cboTurno, spnrHoraEntrada, spnrHoraSalida, cboHorario);
                limpiar();
                JOptionPane.showMessageDialog(rootPane, "Horario de trabajo agregado");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "No se pudo agregar el horario de trabajo --> " + e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarHorarioTrabajoActionPerformed

    private void agregar(JComboBox cboturno, JSpinner horaentrada, JSpinner horasalida, JComboBox horario) throws Exception {
        //Convertir los números de los jspinner a tipo Time:
        int horaentr = Integer.parseInt(horaentrada.getValue().toString());
        int horasal = Integer.parseInt(horasalida.getValue().toString());
        String formatoHora = "%02d:00:00";
        String horaStr1 = String.format(formatoHora, horaentr);
        String horaStr2 = String.format(formatoHora, horasal);
        Time hora1 = Time.valueOf(horaStr1);
        Time hora2 = Time.valueOf(horaStr2);

        String turno = cboturno.getSelectedItem().toString();
        int idtipocont = objTC.obtenerIdTipoContrato(horario.getSelectedItem().toString());

        objHT.registrarHorarioTrabajo(hora1, hora2, idtipocont, turno);
    }

    private void btnEditarHorarioTrabajoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarHorarioTrabajoActionPerformed
        try {
            if (tblHorarioTrabajo.getSelectedRowCount() <= 0) {
                JOptionPane.showMessageDialog(rootPane, "Seleccione un horario de trabajo de la lista");
            } else {
                if (cboTurno.getSelectedIndex() == 0 || cboHorario.getSelectedIndex() == 0) {
                    JOptionPane.showMessageDialog(rootPane, "Por favor, seleccione un turno o un horario válido");
                } else {
                    if (JOptionPane.showConfirmDialog(rootPane, "¿Modificar datos?", "Confirmar cambios", JOptionPane.YES_NO_OPTION)
                            == JOptionPane.YES_OPTION) {
                        modificar(cboTurno, spnrHoraEntrada, spnrHoraSalida, cboHorario,
                                tblHorarioTrabajo.getValueAt(tblHorarioTrabajo.getSelectedRow(), 0).toString());
                        limpiar();
                        JOptionPane.showMessageDialog(rootPane, "Datos modificados");
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "No se pudo modificar el horario de trabajo --> " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditarHorarioTrabajoActionPerformed

    private void modificar(JComboBox cboturno, JSpinner horaentrada, JSpinner horasalida, JComboBox horario, String codigo) throws Exception {
        //Convertir los números de los jspinner a tipo Time:
        int horaentr = Integer.parseInt(horaentrada.getValue().toString());
        int horasal = Integer.parseInt(horasalida.getValue().toString());
        String formatoHora = "%02d:00:00";
        String horaStr1 = String.format(formatoHora, horaentr);
        String horaStr2 = String.format(formatoHora, horasal);
        Time hora1 = Time.valueOf(horaStr1);
        Time hora2 = Time.valueOf(horaStr2);

        String turno = cboturno.getSelectedItem().toString();
        int idtipocont = objTC.obtenerIdTipoContrato(horario.getSelectedItem().toString());
        int idhorariotrabajo = Integer.parseInt(codigo);

        objHT.modificarHorarioTrabajo(hora1, hora2, idtipocont, turno, idhorariotrabajo);
    }

    private void btnEliminarHorarioTrabajoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarHorarioTrabajoActionPerformed
        try {
            if (tblHorarioTrabajo.getSelectedRowCount() <= 0) {
                JOptionPane.showMessageDialog(rootPane, "Seleccione un horario de trabajo de la lista para eliminar");
            } else {
                if (JOptionPane.showConfirmDialog(rootPane, "¿Eliminar horario de trabajo?", "Confirmar cambios", JOptionPane.YES_NO_OPTION)
                        == JOptionPane.YES_OPTION) {
                    if (!objHT.existenRegistrosReferenciados(Integer.parseInt(tblHorarioTrabajo.getValueAt(tblHorarioTrabajo.getSelectedRow(), 0).toString()))) {
                        eliminar(tblHorarioTrabajo.getValueAt(tblHorarioTrabajo.getSelectedRow(), 0).toString());
                        limpiar();
                        JOptionPane.showMessageDialog(rootPane, "Horario de trabajo eliminado");
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "No se puede eliminar. Este horario de trabajo ya se encuentra referenciado "
                                + "en otras tablas");
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "No se pudo agregar el horario de trabajo --> " + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarHorarioTrabajoActionPerformed

    private void eliminar(String codigo) throws Exception {
        int id = Integer.parseInt(codigo);
        objHT.eliminarHorarioTrabajo(id);
    }

