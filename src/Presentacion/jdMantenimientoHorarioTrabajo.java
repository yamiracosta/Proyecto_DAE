    private void cargarCboHorario() throws Exception {
        ResultSet rs = objTC.cargarTipoContratos();
        cboHorario.removeAllItems();
        cboHorario.addItem("---------------------");
        while (rs.next()) {
            cboHorario.addItem(rs.getString("nombre"));
        }
    }

    private void filtrarTurno(JTextField turno) throws Exception {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Hora de entrada");
        modelo.addColumn("Hora de salida");
        modelo.addColumn("Turno");
        modelo.addColumn("Tipo de horario");

        ResultSet rs = objHT.filtrarTurnoHorarioTrabajo(turno.getText());

        Object datos[] = new Object[5];

        while (rs.next()) {
            datos[0] = rs.getInt("id");
            datos[1] = rs.getTime("horaentrada");
            datos[2] = rs.getTime("horasalida");
            datos[3] = rs.getString("turno");
            datos[4] = rs.getString("tipohorario");
            modelo.addRow(datos);
        }
        tblHorarioTrabajo.setModel(modelo);
    }

