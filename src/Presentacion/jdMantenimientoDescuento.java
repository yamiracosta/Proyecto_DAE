    private void btnAgregarDescuentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarDescuentoActionPerformed
        if (txtNombreDescuento.getText().isBlank() || txtDescripcionDescuento.getText().isBlank()) {
            JOptionPane.showMessageDialog(rootPane, "Por favor, complete todos los campos");
        } else {
            try {
                agregarDescuento(txtNombreDescuento.getText(), txtDescripcionDescuento.getText());
                limpiar();
                JOptionPane.showMessageDialog(rootPane, "Descuento registrado");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Error al registrar descuento --> " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnAgregarDescuentoActionPerformed

    private void btnEditarDescuentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarDescuentoActionPerformed
        if (tblDescuento.getSelectedRowCount() > 0) {
            if (txtNombreDescuento.getText().isBlank() || txtDescripcionDescuento.getText().isBlank()) {
                JOptionPane.showMessageDialog(rootPane, "Por favor, complete todos los campos");
            } else {
                try {
                    if (JOptionPane.showConfirmDialog(rootPane, "¿Modificar datos?", "Confirmación de cambios", JOptionPane.YES_NO_OPTION)
                        == JOptionPane.YES_OPTION) {
                        modificarDescuento(txtNombreDescuento.getText(), txtDescripcionDescuento.getText(),
                            Integer.parseInt(tblDescuento.getValueAt(tblDescuento.getSelectedRow(), 0).toString()));
                        limpiar();
                        JOptionPane.showMessageDialog(rootPane, "Datos modificados");
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, "Error al modificar descuento --> " + e.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un descuento de la lista");
        }
    }//GEN-LAST:event_btnEditarDescuentoActionPerformed

    private void btnEliminarDescuentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarDescuentoActionPerformed
        if (tblDescuento.getSelectedRowCount() <= 0) {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un descuento de la lista para eliminar");
        } else {
            try {
                if (JOptionPane.showConfirmDialog(rootPane, "¿Eliminar descuento?", "Confirmación de cambios", JOptionPane.YES_NO_OPTION)
                    == JOptionPane.YES_OPTION) {
                    String codigo = tblDescuento.getValueAt(tblDescuento.getSelectedRow(), 0).toString();
                    if (!obj.existenRegistrosRelacionados(Integer.parseInt(codigo))) {
                        eliminarDescuento(Integer.parseInt(codigo));
                        limpiar();
                        JOptionPane.showMessageDialog(rootPane, "Descuento eliminado");
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "No se puede eliminar. El descuento ya está referenciado en otras tablas");
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Error al eliminar descuento --> " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarDescuentoActionPerformed

